#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	k;

	if (ac > 1)
	{
		i = ac - 1;
		while (i > 0)
		{
			k = 0;
			while (av[i][k])
			{
				write (1, &av[i][k], 1);
				k++;
			}
			write (1, "\n", 1);
			i--;
		}
	}
	return (0);
}
